hpc :: TestProject3DFile
   :: TestProject3DFile(const std::string & fileName)
   : m_fileName(fileName)
   , m_project()
{
  write( fileName );
  m_factory.reset( new DataAccess::Interface::ObjectFactory());
  m_project.reset( DataAccess::Interface::OpenCauldronProject( fileName, m_factory.get() ));
}

hpc :: TestProject3DFile
   :: ~TestProject3DFile()
{
   m_project.reset();
   std::remove( m_fileName.c_str() );
}

DataAccess :: Interface :: ProjectHandle *
hpc :: TestProject3DFile
   :: project()
{
   return m_project.get();
}

void
hpc :: TestProject3DFile
   :: write( const std::string & fileName)
{
  std::ofstream output( fileName.c_str() );

  if (!output)
     throw WriteException() << "Could not open file '" << fileName << "' for output";

  output << s_contents;

  output.close();

  if (!output)
     throw WriteException() << "Could not write file '" << fileName << "'.";
}



